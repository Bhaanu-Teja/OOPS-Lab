public class Avg_Of_Three_Numbers {
    public static void main(String[] args) {
        double num1 = 10.5;
        double num2 = 20.3;
        double num3 = 30.7;

        double average = (num1 + num2 + num3) / 3;

        System.out.println("The average of the three numbers is: " + average);
    }
}
